    private void botonAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAsignarActionPerformed
        int chosenIndex = jListManagers.getSelectedIndex();
        if (chosenIndex == -1) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar el manager que será asignado", "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Manager manager = managers.get(chosenIndex);
        if (manager == null) {
            JOptionPane.showMessageDialog(this, "No se ha podido determinar el manager seleccionado", "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            CallableStatement stm = festival.getConnection().prepareCall("{call AsignarGrupoManager(?, ?)}");
            stm.setString(1, grupo.getId());
            stm.setString(2, manager.getId());

            stm.execute();

            festival.getConnection().commit();
            
            JOptionPane.showMessageDialog(this, "Se ha asignado el manager", "Manager asignado", JOptionPane.INFORMATION_MESSAGE);
            java.awt.EventQueue.invokeLater(() -> {
                this.setVisible(false);
            });
        } catch (SQLException ex) {
            System.err.println(ex);
            JOptionPane.showMessageDialog(this, ex.toString(), "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonAsignarActionPerformed

