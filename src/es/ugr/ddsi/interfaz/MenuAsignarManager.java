    public MenuAsignarManager(MainFestival festival, Grupo grupo) {
        this.festival = festival;
        this.grupo = grupo;
        initComponents();
        
        try {
            PreparedStatement stm = festival.getConnection().prepareStatement("SELECT * FROM MANAGER");
            ResultSet rs = stm.executeQuery();
            
            managers = new ArrayList<>();
            while (rs.next()) {
                managers.add(new Manager(
                        rs.getString("id_manager"),
                        rs.getString("nombre"),
                        rs.getString("email"),
                        rs.getString("telefono")
                ));
            }
            
            jListManagers = new javax.swing.JList(managers.toArray());
            scrollManagers.setViewportView(jListManagers);
        } catch (SQLException ex) {
            System.err.println(ex);
        }
    }

