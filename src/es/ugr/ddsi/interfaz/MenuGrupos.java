    private void botonOfertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonOfertarActionPerformed
        int chosenIndex = listaGrupos.getSelectedIndex();
        if (chosenIndex == -1) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar un grupo al que ofertar", "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Grupo ofertado = grupos.get(chosenIndex);
        if (ofertado == null) {
            JOptionPane.showMessageDialog(this, "No se ha podido determinar el grupo seleccionado", "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        java.awt.EventQueue.invokeLater(() -> {
            new MenuOfertarGrupo(festival, ofertado).setVisible(true);
        });
    }//GEN-LAST:event_botonOfertarActionPerformed

