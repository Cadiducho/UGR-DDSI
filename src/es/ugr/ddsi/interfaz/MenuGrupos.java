    public MenuGrupos(MainFestival festival) {
        this.festival = festival;
        initComponents();
        
        try {
            PreparedStatement stm = festival.getConnection().prepareStatement("SELECT * FROM GRUPO");
            ResultSet rs = stm.executeQuery();
            
            grupos = new ArrayList<>();
            while (rs.next()) {
                grupos.add(new Grupo(
                        rs.getString("id_grupo"),
                        rs.getString("nombre_grupo"),
                        rs.getString("email_grupo"),
                        rs.getString("telefono_grupo"),
                        rs.getInt("cache_grupo")
                ));
            }
            
            listaGrupos = new javax.swing.JList(grupos.toArray());
            scrollGrupos.setViewportView(listaGrupos);
        } catch (SQLException ex) {
            System.err.println(ex);
        }
        
    }

